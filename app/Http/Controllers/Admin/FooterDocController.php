    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $doc = Doc::find($id);
        \Storage::delete($doc->url);
        $doc->delete();

        return redirect()->route('footer-doc.index')->with('success', 'Успешно удален');
    }
